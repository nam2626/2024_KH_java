
public class E04_Operator4 {
	/*
	 * 		관계(비교) 연산자 --> 명제, 조건식
	 * 			결과는 true/false로 나온다.
	 * 
	 * 		>	<	>=	<=	==	!=
	 * 
	 */
	public static void main(String[] args) {
		int n1 = 10, n2 = 7;
		
		System.out.println(n1 > n2);
		System.out.println(n1 < n2);
		System.out.println(n1 >= n2);
		System.out.println(n1 <= n2);
		System.out.println(n1 == n2);
		System.out.println(n1 != n2);
	}

}







